    private void txtnombresKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtnombresKeyTyped
        
        char ca = evt.getKeyChar();
        if ((ca < 'a' || ca > 'z') && (ca < 'A' || ca > 'Z')
                && ca != 'á' //Minúsculas            
                && ca != 'é'
                && ca != 'í'
                && ca != 'ó'
                && ca != 'ú'
                && ca != 'Á' //Mayúsculas            
                && ca != 'É'
                && ca != 'Í'
                && ca != 'Ó'
                && ca != 'Ú'
                && (ca != (char) KeyEvent.VK_SPACE)) {
            evt.consume();
        }
    }//GEN-LAST:event_txtnombresKeyTyped

