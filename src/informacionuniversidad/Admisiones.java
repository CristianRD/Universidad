    private void btnabrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnabrirActionPerformed
        //Guardando el nombre del archivo que desea abrir      
  String nombre = JOptionPane.showInputDialog("Escribe el nombre del archivo:"); 
  File archivo = null;
  FileReader fr = null;
  BufferedReader br = null;
      
    try 
      {
        //Se instancia el archivo
        archivo = new File (nombre);

        //Podemos abrir un fichero de texto para leer usando la clase FileReader.
        fr = new FileReader (archivo); 

        //Se instancia el buffer, para almacenar datos de lectura
        br = new BufferedReader(fr);

        /*Utilizaremos esta variable junto con el while, para leer cada linea
          del archivo*/
        String linea;
        while((linea=br.readLine())!=null)
           {
            JOptionPane.showMessageDialog(null,linea);
           } 
         
         /*En caso tal de que no existe ningun archivo con el nombre, o el 
           nombre este mal escrito*/
       }
    catch (FileNotFoundException ex) 
       {
           // Logger.getLogger(Registrar.class.getName()).log(Level.SEVERE, null, ex);
          JOptionPane.showMessageDialog(null,"No existe ningun archivo con este"
                  + " nombre, recuerde escribir al final del archivo sin espacios"
                  + " .txt");  
       }
    catch (IOException ex) 
       {
         Logger.getLogger(Admisiones.class.getName()).log(Level.SEVERE, null, ex);
       }
    catch (NullPointerException e)
       {
        // Si llega a dar cancelar al inputdialog, no generar nada
       }
      
    }//GEN-LAST:event_btnabrirActionPerformed

